import { readdirSync, statSync, writeFileSync, existsSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const articlesDir = join(__dirname, '..', 'src', 'pages', 'articles')
const outFile = join(__dirname, '..', 'src', 'lib', 'articles.generated.js')

const entries = readdirSync(articlesDir)
  .map((name) => {
    const fullPath = join(articlesDir, name)
    if (statSync(fullPath).isDirectory()) {
      const indexMdx = join(fullPath, 'index.mdx')
      if (existsSync(indexMdx)) {
        return { slug: name, importPath: `${name}/index.mdx` }
      }
      return null
    }
    if (name.endsWith('.mdx')) {
      return { slug: name.replace(/\.mdx$/, ''), importPath: name }
    }
    return null
  })
  .filter(Boolean)
  .sort((a, z) => a.slug.localeCompare(z.slug))

const imports = entries
  .map((e, i) => `import * as a${i} from '@/pages/articles/${e.importPath}'`)
  .join('\n')

const modules = entries
  .map((e, i) => `  { slug: '${e.slug}', module: a${i} }`)
  .join(',\n')

const content = `// This file is auto-generated by scripts/generate-articles.mjs.
// Do not edit directly.
${imports}

export const articleModules = [
${modules},
]
`

writeFileSync(outFile, content)
console.log(`Generated ${outFile} with ${entries.length} articles`)
